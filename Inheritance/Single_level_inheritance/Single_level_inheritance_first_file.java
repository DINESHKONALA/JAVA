package Inheritance.Single_level_inheritance;

public class Single_level_inheritance_first_file {
    public int add(int a, int b) {
        return a + b;
    }   
    
    public int sub(int a, int b) {
        return a - b;
    }
    
    public int mul(int a, int b) {
        return a * b;
    }
}
